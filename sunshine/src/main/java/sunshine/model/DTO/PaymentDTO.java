package sunshine.model.DTO;

public class PaymentDTO {

	String payNum;
	String mOrderNum;
	String deliveryAddress;
	String payDivision;
	String depositChk;
	String goodsNum;
	String goodsCategori;
	String goodsContent;
	String goodsImage;
	public String getPayNum() {
		return payNum;
	}
	public void setPayNum(String payNum) {
		this.payNum = payNum;
	}
	public String getmOrderNum() {
		return mOrderNum;
	}
	public void setmOrderNum(String mOrderNum) {
		this.mOrderNum = mOrderNum;
	}
	public String getDeliveryAddress() {
		return deliveryAddress;
	}
	public void setDeliveryAddress(String deliveryAddress) {
		this.deliveryAddress = deliveryAddress;
	}
	public String getPayDivision() {
		return payDivision;
	}
	public void setPayDivision(String payDivision) {
		this.payDivision = payDivision;
	}
	public String getDepositChk() {
		return depositChk;
	}
	public void setDepositChk(String depositChk) {
		this.depositChk = depositChk;
	}
	public String getGoodsNum() {
		return goodsNum;
	}
	public void setGoodsNum(String goodsNum) {
		this.goodsNum = goodsNum;
	}
	public String getGoodsCategori() {
		return goodsCategori;
	}
	public void setGoodsCategori(String goodsCategori) {
		this.goodsCategori = goodsCategori;
	}
	public String getGoodsContent() {
		return goodsContent;
	}
	public void setGoodsContent(String goodsContent) {
		this.goodsContent = goodsContent;
	}
	public String getGoodsImage() {
		return goodsImage;
	}
	public void setGoodsImage(String goodsImage) {
		this.goodsImage = goodsImage;
	}
	
	
}
