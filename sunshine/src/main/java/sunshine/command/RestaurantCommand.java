package sunshine.command;

public class RestaurantCommand {
	
	String menuNum;
	String menuName;
	String menuPrice;
	String menuPhoto;
	String menuIllu;
	
	public String getMenuNum() {
		return menuNum;
	}
	public void setMenuNum(String menuNum) {
		this.menuNum = menuNum;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuPrice() {
		return menuPrice;
	}
	public void setMenuPrice(String menuPrice) {
		this.menuPrice = menuPrice;
	}
	public String getMenuPhoto() {
		return menuPhoto;
	}
	public void setMenuPhoto(String menuPhoto) {
		this.menuPhoto = menuPhoto;
	}
	public String getMenuIllu() {
		return menuIllu;
	}
	public void setMenuIllu(String menuIllu) {
		this.menuIllu = menuIllu;
	}
	
	

}
